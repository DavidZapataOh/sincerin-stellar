/*
 * export-benchmark — Option C: write the static benchmark assets for the README
 * (judges who evaluate from GitHub without opening the app). Generates the two
 * chart SVGs from the SAME pure generators the /benchmarks page uses (no drift),
 * and splices a generated section into README.md between the BENCH markers.
 *
 * Run: cd frontend && npm run bench:export
 */

import { writeFileSync, readFileSync } from "node:fs";
import { resolve, dirname } from "node:path";
import { fileURLToPath } from "node:url";
import { crossoverChartSVG, provingChartSVG } from "../src/lib/benchmarkChart";
import {
  ROWS,
  AGGREGATE_CLAIM,
  CROSSOVER_N_INT,
  BUDGET,
  formatProving,
  pctOfBudget,
  type Flag,
} from "../src/lib/benchmark";

const here = dirname(fileURLToPath(import.meta.url));
const repo = resolve(here, "../..");
const docs = resolve(repo, "docs");

// 1 · the two chart SVGs (white-backed so they read in GitHub light AND dark)
writeFileSync(resolve(docs, "benchmark-crossover.svg"), crossoverChartSVG({ w: 760, h: 480 }));
writeFileSync(resolve(docs, "benchmark-proving.svg"), provingChartSVG({ w: 760, h: 360 }));

// 2 · the two-axis table, generated from the source of truth
const commas = (n: number) => n.toLocaleString("en-US");
const flag = (f: Flag) => (f === "measured" ? "**measured**" : "projected");
const tableRows = ROWS.map((r) => {
  const gpu =
    r.provingGpuSeconds != null
      ? `${formatProving(r.provingGpuSeconds)} (${flag(r.provingGpuFlag ?? "measured")})`
      : "—";
  const fits = r.baselineInsn <= BUDGET ? "fits" : "**won’t fit**";
  return `| ${r.n} | ${r.depth} | ${commas(r.cycles)} | ${formatProving(r.provingCpuSeconds)} (${flag(
    r.provingCpuFlag,
  )}) | ${gpu} | ${(r.settleAggInsn / 1e6).toFixed(1)}M (${pctOfBudget(r.settleAggInsn).toFixed(1)}%, ${flag(
    r.settleFlag,
  )}) | ${(r.baselineInsn / 1e6).toFixed(0)}M (${fits}) |`;
}).join("\n");

const table = `| N | depth | cycles (measured) | proving · CPU (dev) | proving · GPU (prod) | settle (aggregated) | baseline · N×35M |
|---|---|---|---|---|---|---|
${tableRows}`;

const section = `
> ${AGGREGATE_CLAIM}

![On-chain cost vs batch size: the baseline of N separate verifications crosses the 400M one-transaction budget around N=${CROSSOVER_N_INT} and becomes infeasible, while the aggregated Sincerin settle stays inside the budget but grows sub-linearly.](docs/benchmark-crossover.svg)

**The differentiator in one image:** settling N withdrawals as N separate Groth16 verifications crosses the 400M single-transaction budget around **N ≈ ${CROSSOVER_N_INT}** and stops being possible at all. Aggregated into one receipt, the on-chain cost grows sub-linearly (it is **not** flat) and always fits.

${table}

Off-chain, proving grows with N — but in production it is **minutes, not hours**: a real N=8 proof on a single RTX 3090 (native CUDA) took **${formatProving(
  304,
)}**, ~52× faster than the Mac development figure of 4h 26m. The Mac hours are an x86-on-ARM emulation artifact, not the production number.

![Off-chain proving time vs N on a log scale: CPU development hours versus GPU production minutes.](docs/benchmark-proving.svg)

Every cell is **measured** or projected — never mixed without a label (full provenance and method in [docs/proving-times.md](docs/proving-times.md)). A live, interactive version is at **/benchmarks** in the app.
`;

// 3 · splice into README between the markers (idempotent, re-runnable)
const readmePath = resolve(repo, "README.md");
const START =
  "<!-- BENCH:START (generated by `cd frontend && npm run bench:export` — do not edit by hand) -->";
const END = "<!-- BENCH:END -->";
const escapeRegExp = (s: string) => s.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
const re = new RegExp(`${escapeRegExp(START)}[\\s\\S]*?${escapeRegExp(END)}`);
let readme = readFileSync(readmePath, "utf8");
if (!re.test(readme)) {
  throw new Error("README BENCH markers not found — add them first.");
}
readme = readme.replace(re, `${START}\n${section}\n${END}`);
writeFileSync(readmePath, readme);

console.log("exported: docs/benchmark-crossover.svg, docs/benchmark-proving.svg, README section");
